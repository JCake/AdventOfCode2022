    private static class Point {
        int x;
        int y;

        public Point(int x, int y) {
            this.x = x;
            this.y = y;
        }
    }

    // Modifier to "simulate" going infinite in either direction - make this as big as needed to avoid out of bounds
    private static final int X_MODIFIER = 500;

    public static void main(String[] args){
        String[] formationStrs = PUZZLE.split("\n");
        int maxX = 0;
        int maxY = 0;
        List<List<Point>> formations = new ArrayList<>();
        for(String formation : formationStrs){
            String[] pointStrs = formation.split(" -> ");
            List<Point> points = new ArrayList<>();
            for(String point : pointStrs){
                String[] xAndY = point.split(",");
                int x = parseInt(xAndY[0]);
                int y = parseInt(xAndY[1]);
                maxX = Math.max(x, maxX);
                maxY = Math.max(y, maxY);
                points.add(new Point(x, y));
            }
            formations.add(points);
        }

        int gridMaxY = maxY + 2;
        int gridMaxX = maxX + X_MODIFIER * 2;
        char[][] grid = new char[gridMaxY + 1][gridMaxX + 1];
        for(int y = 0; y < gridMaxY; y++){
            for(int x = 0; x <= gridMaxX; x++){
                grid[y][x] = ' ';
            }
        }
        for(int x = 0; x <= gridMaxX; x++){
            grid[gridMaxY][x] = '#'; // ' ' for part 1
        }

        for(List<Point> formation : formations){
            for(int i = 1; i < formation.size(); i++){
                Point a = formation.get(i - 1);
                Point b = formation.get(i);
                if(a.x == b.x){
                    int x = a.x;
                    int startY = Math.min(a.y, b.y);
                    int endY = Math.max(a.y, b.y);
                    for(int y = startY; y <= endY; y++){
                        grid[y][x + X_MODIFIER] = '#';
                    }
                } else {
                    int y = a.y;
                    int startX = Math.min(a.x, b.x);
                    int endX = Math.max(a.x, b.x);
                    for(int x = startX; x <= endX; x++){
                        grid[y][x + X_MODIFIER] = '#';
                    }
                }
            }
        }

        int grainsFallen = calculateGrainsFallen(grid);
        System.out.println(grainsFallen);

    }

    private static int calculateGrainsFallen(char[][] grid) {
        int grainsFallen = 0;
        int currSandX = 500;
        int currSandY = 0;
        // Part 1 had condition of currSandY + 1 < grid.length
        while(grid[0][500 + X_MODIFIER] != 'o'){
            if(grid[currSandY + 1][currSandX + X_MODIFIER] == ' '){
                currSandY++;
            } else if(grid[currSandY + 1][currSandX - 1 + X_MODIFIER] == ' ') {
                currSandX--;
                currSandY++;
            } else if(grid[currSandY + 1][currSandX + 1 + X_MODIFIER] == ' ') {
                currSandX++;
                currSandY++;
            } else {
                grid[currSandY][currSandX + X_MODIFIER] = 'o';
                grainsFallen++;
                currSandX = 500;
                currSandY = 0;
            }
        }
        return grainsFallen;
    }

